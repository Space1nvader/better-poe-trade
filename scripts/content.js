const getFavorites = () => {
    const favorites = window.localStorage.getItem('favorites')
    return JSON.parse(favorites || '[]') 
}

const addFavorite = (value) => {
    const favorites = getFavorites()
    favorites.push(value)

    console.log('favorites', favorites)
    window.localStorage.setItem('favorites', JSON.stringify(favorites));
}

// ПРоверка на существование поиска в избранном
const saveFavoriteSearch = (name) => {
        const favorites = getFavorites()

        const targetName = name.trim();
        const isExist = favorites.length && favorites.some((item) => item.name.toLowerCase() === targetName.toLowerCase());
        if(isExist){
            return 0
        } else {
            addFavorite({name, link: window.location.href})
           return 1
        }

}

const createFavoriteItem = (item) => {
    const deleteFavoriteItem = document.createElement('button');

    const deleteFavoriteHandler= (parent) => {
        const favorites = getFavorites()
      
        const newFavorites = favorites.filter(favorite => favorite.name !== item.name)

        window.localStorage.setItem('favorites', JSON.stringify(newFavorites));
        parent.remove();
    }

    const editFavoriteHandler = () => {
        console.log('редактировать');
    }

    deleteFavoriteItem.className = 'favoriteItemAction';
    deleteFavoriteItem.title = 'удалить';
    deleteFavoriteItem.innerHTML = svgdeleteicon;
    deleteFavoriteItem.addEventListener('click', (event) => {
        deleteFavoriteHandler(event.target.closest('.favoriteItem'))
    });

    // КНОПКА РЕДАКТИРОВАНИЯ ФИЛЬТРА
    const editFavoriteItem = document.createElement('button');
    editFavoriteItem.className = 'favoriteItemAction';
    editFavoriteItem.title = 'редактировать';
    editFavoriteItem.innerHTML = svgpencilicon;
    editFavoriteItem.addEventListener('click', (event) => {
        editFavoriteHandler()
    });

    // САМ ЭЛЕМЕНТ ФИЛЬТРа
    const favoriteItem = document.createElement('div');
    favoriteItem.className = 'favoriteItem';
    favoriteItem.innerHTML = `
      <a href="${item.link}" class="favoriteItemLink">
        <span class="favoriteItemIcon">${item?.icon || 'i'}</span>
        ${item.name}
      </a>
        <div class="favoriteItemActions"></div>`;

       const actions = favoriteItem.querySelector('.favoriteItemActions');
       actions.append(editFavoriteItem);
       actions.append(deleteFavoriteItem);

    return favoriteItem;
};



